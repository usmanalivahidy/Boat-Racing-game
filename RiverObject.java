package GroupAssignment;


public class RiverObject{
	
	private int location;
	private String symbol;
	

	public int getLocation() { //returns the location of river objects
		return location;
	}

	public void setLocation(int location) { //sets location of river objects
		this.location = location;
	}
	
	public String getSymbol() { // returns the symbol used by the river object
		return symbol;
	}

	public void setSymbol(String symbol) { // sets the symbol used by the river object
		this.symbol = symbol;
	}
	
	


}
